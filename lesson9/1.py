"""
Написать функцию print_n() которая будет печатать переданный текст, 
но при этом перед этим текстом выводить строку с номером отражающим 
какой раз по счету выполняется эта функция. 

"""


def print_n(text):
    """Печатает текст с порядковым номером вызова."""
    print_n.counter = getattr(print_n, "counter", 0) + 1
    print(f"{print_n.counter}. {text}")


if __name__ == "__main__":
    print_n("Первое сообщение")
    print_n("Второе сообщение")
    print_n("Третье сообщение")