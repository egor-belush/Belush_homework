'''
Запросить число от 1 до 12. 
Если ввели другое число сообщить об ошибке. 
Если ввели не число сообщить об ошибке. 
Когда введут допустимое число - вывести на экран соответствующее 
название месяца, пору года и сколько дней в данном месяце.

'''


months = {
    1: ("Январь", "Зима", 31),
    2: ("Февраль", "Зима", 28),  # Високосный год не учитывается
    3: ("Март", "Весна", 31),
    4: ("Апрель", "Весна", 30),
    5: ("Май", "Весна", 31),
    6: ("Июнь", "Лето", 30),
    7: ("Июль", "Лето", 31),
    8: ("Август", "Лето", 31),
    9: ("Сентябрь", "Осень", 30),
    10: ("Октябрь", "Осень", 31),
    11: ("Ноябрь", "Осень", 30),
    12: ("Декабрь", "Зима", 31)
}

while True:
    user_input = input("Введите число от 1 до 12 (номер месяца): ")
    try:
        month_num = int(user_input)
        if month_num < 1 or month_num > 12:
            print("Ошибка: число вне диапазона 1-12.")
            continue
    except ValueError:
        print("Ошибка: введено не число.")
        continue
    
    month_name, season, days = months[month_num]
    print(f"Месяц: {month_name}, Пора года: {season}, Количество дней: {days}")
    
