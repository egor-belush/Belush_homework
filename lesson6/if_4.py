'''
Запросить у пользователя число
    - если число менее 20 -  вывести на экран сколько чисел 
        в диапазоне от 0 до этого числа делится без остатка на 7. 
    - если более 20 - вывести на экран сколько чисел 
        в диапазоне от 0 до этого числа делится без остатка на 11.
'''


number = int(input("Введите число: "))

# Проверяем условие
if number < 20:
    # Для чисел, делящихся на 7
    count = number // 7 + 1  # +1 потому что 0 тоже делится на 7
    print(f"В диапазоне от 0 до {number} чисел, делящихся на 7: {count}")
else:
    # Для чисел, делящихся на 11
    count = number // 11 + 1  # +1 потому что 0 тоже делится на 11
    print(f"В диапазоне от 0 до {number} чисел, делящихся на 11: {count}")