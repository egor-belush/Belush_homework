'''
Запросить высоту елочки - число от 3 до 20. 
Напечатать на экране елочку где ее высота равна числу строк. 
Пример елочки из 4 строк:
   *
  ***
 *****
*******

* - елочка со снегом
'''

# Запрашиваем высоту елочки
while True:
    try:
        height = int(input("Введите высоту елочки (от 3 до 20): "))
        if 3 <= height <= 20:
            break
        else:
            print("Ошибка: число должно быть от 3 до 20")
    except ValueError:
        print("Ошибка: введите целое число")

# Рисуем елочку
print("\nВаша елочка:")
for i in range(height):
    # Количество звездочек в строке: 1, 3, 5, 7, ...
    stars = 2 * i + 1
    # Количество пробелов слева для центрирования
    spaces = height - i - 1
    # Выводим строку
    print(" " * spaces + "*" * stars)