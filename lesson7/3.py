'''
Запросить любое число. Заменить каждую цифру этого числа буквой, 
у которой номер в алфавите равен этой цифре. 
Алфавит считаем от 0. a-0, b-1, c-3 и т.д.
Например: 13520 -> bdfca.
'''

alphabet = "abcdefghijklmnopqrstuvwxyz"

# Запрашиваем число у пользователя
number = input("Введите число: ")

# Проверяем, что введены только цифры
if not number.isdigit():
    print("Ошибка: введите только цифры!")
else:
    # Преобразуем каждую цифру в букву
    result = ""
    
    # Проходим по каждой цифре в числе
    for digit_char in number:
        # Преобразуем символ цифры в число
        digit = int(digit_char)
        
        # Проверяем, что цифра в диапазоне 0-25 (индексы алфавита)
        if 0 <= digit <= 25:
            # Находим соответствующую букву в алфавите
            letter = alphabet[digit]
            result += letter
        else:
            # Если цифра больше 25, оставляем её как есть
            result += digit_char
    
    print(f"Результат: {result}")