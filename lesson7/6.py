"""
1. Запросить у пользователей имя и отзыв о магазине. 
Программа должна запрашивать данные пока не введено слово "stop". 
Все данные сложить в словарь.
    -распечатать количество отзывов
    -распечатать отдельно имена пользователей
    -распечатать отдельно отзывы

"""

# Создаем пустой словарь для хранения отзывов
reviews = {}

print("Введите имена и отзывы о магазине (для завершения введите 'stop'):")
print("=" * 50)

# Бесконечный цикл для ввода данных
while True:
    # Запрашиваем имя
    name = input("\nВведите имя: ")
    
    # Проверяем, не введено ли слово "stop"
    if name.lower() == "stop":
        break
    
    # Запрашиваем отзыв
    review = input("Введите отзыв: ")
    
    # Проверяем, не введено ли слово "stop" в отзыве
    if review.lower() == "stop":
        break
    
    # Добавляем данные в словарь
    reviews[name] = review

# Выводим статистику
print("\n" + "=" * 50)
print("СТАТИСТИКА ОТЗЫВОВ:")
print("=" * 50)

# 1. Количество отзывов
print(f"1. Количество отзывов: {len(reviews)}")

# 2. Имена пользователей
print(f"2. Имена пользователей: {list(reviews.keys())}")

# 3. Отзывы
print(f"3. Отзывы: {list(reviews.values())}")

# Дополнительно: выводим все данные построчно
print("\n" + "=" * 50)
print("ПОЛНЫЙ СПИСОК ОТЗЫВОВ:")
print("=" * 50)
for name, review in reviews.items():
    print(f" {name}: {review}")