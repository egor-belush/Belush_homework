'''
Дан произвольный список из 5 элементов. 
     - Поменять местами 1ый и последний элемент
     - удалить и вывести на печать третий элемент
'''


my_list = [10, 20, 30, 40, 50]

print("Исходный список:")
print(my_list)

# Меняем местами первый и последний элемент
my_list[0], my_list[-1] = my_list[-1], my_list[0]

print("\nПосле замены первого и последнего элемента:")
print(my_list)

# Удаляем и выводим третий элемент 
deleted_element = my_list.pop(2)
print(f"\nУдаленный третий элемент: {deleted_element}")

print("Список после удаления третьего элемента:")
print(my_list)